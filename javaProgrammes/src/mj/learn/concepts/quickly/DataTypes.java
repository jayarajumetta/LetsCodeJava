public class DataTypes {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		//Print Hello World on console
		System.out.println("Hello World");
		
		//String DataType
		//Declare a variable called printHelloworld
		String printHelloworld;
		
		//Initialize the printHelloworld variable 
		printHelloworld="HelloWorld";
		
		//Print printHelloworld variable value on console
		System.out.println(printHelloworld);
		
		//int Data Type
		int count ;
		count=2;
		System.out.println(count);
		
		//Bollean DataType
		boolean displaypresence ;
		displaypresence=true;
		System.out.println(displaypresence);
		
		boolean displaypresence1 =false;
		System.out.println(displaypresence1);
		
		//char Data Type
		char printCharacter;
		printCharacter='M';
		System.out.println(printCharacter);
		
		//double DataType
		double doubleDataType;
		doubleDataType=2.6;
		System.out.println(doubleDataType);
		
		//float dataType
		float floatVariable;
		floatVariable=2.5f;
		System.out.println(floatVariable);
		
		//cast float variable Type
		float castFloat;
		castFloat= (float) 2.6;
		System.out.println(castFloat);
		
		//short data type
		
		short shortVariable=56;
		System.out.println(shortVariable);
		
		//long data type
		long longDataType=565645555;
		System.out.println(longDataType);

	}




}
